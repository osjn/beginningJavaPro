class MathTester
{
    public static void main(String[] args) {
        double num1 = 2.34;
        double num2 = 1.56;

        System.out.println(Math.max(num1, num2));
        System.out.println(Math.min(num1, num2));
        System.out.println(Math.sqrt(num1));
        System.out.println(Math.pow(num1, num2));
    }
}
